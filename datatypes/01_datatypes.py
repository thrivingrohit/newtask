# immutable datatypes 

a=10
print(a, type(a))
b=5.5
print(b, type(b))
c=1+2j
print(c, type(c))

# multiple line strings can be denoted using triple quotes, ''' or """

# string 

s="Hello, Rohit!!!"
print(s,type(s))

p=""""Hello 
      rohit parashar
      B.tech
"""
print(p,type(p))

# tuple 

tuple=(5, 6.4, 'itm', 1+4j)
print(tuple,type(tuple))

#set

set={1,2,3,3,4.5}
print(set,type(set))